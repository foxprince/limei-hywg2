<h1>何谓色级？</h1>

<div class="field-item even">
<img src="../images/site_elements/howtochoose/color.jpg" width="680" alt="何谓色级？" />
<p>大多数钻石的颜色为无色或微黄，但钻石也会以其他颜色出现，例如红色、蓝色或粉红色等颜色的彩钻。从传统意义上来讲无色钻石中最高等级的颜色是D色，之后的则是E、F、G、H、I、J、K、L等，等级越向后则颜色越黄。</p>
<p>钻石颜色的分级是十分困难的，尤其是高等级的D、E、F色等，只有在专业钻石实验室全封闭的暗室中，6500度色温的钻石灯的照射下，经过专业的钻石分级师和比色石进行对比之后才能判断出钻石本身的颜色属于什么级别。如果是镶嵌后的钻石，在日光下别说D 、E、色的钻石，就即便I 、J、 K、色在普通人眼中的区别都是很细微的。</p>
