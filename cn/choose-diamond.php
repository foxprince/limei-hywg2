<h1>证书区别</h1>
<div class="field-item even">
<img src="../images/site_elements/howtochoose/HRD.jpg" alt="证书区别" /></div>

<p>在当今最流行的几种钻石证书当中，其中之一便是来自于欧洲大名鼎鼎的HRD证书了，那HRD是什么意思呢？ HRD的全称是Hoge Raad voor Diamant，比利时高等钻石议会。比利时钻石高等议会代表着欧洲最权威的宝石认证水平，也是首家得到ISO国际标准化组织认证的钻石分级机构，这自然赋予了HRD证书不容置疑的权威性。 </p>
<p>那钻石证书是怎么来的呢？自从20世纪70年代，钻石投资一直是一项获利颇丰的行业。不过在此之前，在钻石行业当中没有大家都认可的钻石证书。在20世纪 70年代初钻石投资变得流行起来，这使得钻石价格大升，尤其是那些颜色和净度都比较好的钻石。由于钻石原石商贸结构的优化使得钻石贸易更加有保障，加上钻石价格地不断上涨，使得钻石投资越发具吸引力。一些行外人士开始转向开发钻石业这个新兴市场。<br />
  一些机构为了刺激钻石消费而建立了一系列的实验室并开始制作 证书以哄抬价格，这些钻石实验室就像雨后春笋般的涌现出来，大多数实验室都有一些自己的标准以及系统命名法，但是这些证书完全没有安全保证，而且实验室之间并不互相认可。自然而然买家就成为受害者，钻石证书的信誉严重下降。<br />
在1972年，世界钻石交易所联盟（WFDB）和国际钻石制造商协会（IDMA）在 安特卫普共同举办了一次国际议会，讨论了关于钻石标准的问题，但是这次的会议并没有得到很积极的结果。钻石商人们不信任这些钻石鉴定证书，而且更可怕的是由于一些政客的干涉，导致人们对钻石标准的怀疑。</p>
<img src="../images/site_elements/howtochoose/HRD_1.jpg.jpg" alt="证书区别" />
<p>1974 年，比利时钻石高等议会(Hoge Raad voor Diamant)就同意科学技术研究中心成立一个鉴定证书办公室。他们开始建立一套能复核的、客观的标准。这项研究在安特卫普进行，所获得的经验成为了之 后联合委员会协商的基础，并同时出版了&quot;钻石说明书标准化方案（Project for Normalising the Specifications of Diamond）&quot;。1975年，钻石议会废除许多不合格实验室可疑的标准和系统命名法，并且成立了由WFDB和IDMA代表组成的联合委员会负责研究出 现的问题。并达成了以下决议：<br />
  1. 必须建立一套世界组织都认可的统一的标准。<br />
  2. 这些标准必须使用统一的方法，在任何情况下都具有可复核性。<br />
  3. 必须建立被隶属IDMA和WFDB的专业机构认证的实验室。<br />
HRD和CIBJO(国际金银珠宝联盟)的联合委员会中的代表们建立的标准化系统命名法以及标准在议会之后正式被其它组织接受了。这一套原始的钻石分级标准母版被保存在了安特卫普。</p>
<img src="../images/site_elements/howtochoose/HRD_2.jpg" alt="证书区别" />
<p>为了能够充分的评价钻石的魅力，所有涉及到钻石的细节都可以在HRD证书中找到。一本证书只有在它值得信赖且被认可时才是有用的：HRD证书以其客观性、可 信度和品质而享有盛誉。1996年2月，在成品钻石品质分级方面，HRD证书部门被授予了ISO 9002品质印章。时至今日，HRD钻石证书早已得到了世界上大多国家和机构的正式承认。如果您购买了HRD认证的钻石，你可以通过HRD的官方网 站 http://www.hrdantwerplink.be/ 输入您所购买的钻石克拉重量以及证书的序列号来检验您证书的真实性。<br />
除了HRD证书之外，国际上还有其他的几种证书也在钻石市场上得到了同样的认可，例如美国宝石学会（GIA）所提供的钻石分级证书</p>

<img src="../images/site_elements/howtochoose/GIA.png" alt="证书区别" />

<p>GIA 的全称是Gemological Institute of America， 美国宝石学会。美国宝石学会成立于1931年，跟比利时钻石高级议会（HRD）比起来算是后起之秀了。在那个年代，很大一部珠宝商并没有那么了解他们手中的各种宝石。一个精明的珠宝商人Robert M. Shipley发现了其中的机会，于是便用尽自己的存款在美国洛杉矶建立起了这个为宝石行业提供专业化的培训、鉴定以及相关的设备。</p>
<p>虽然美国宝石学会早在1931年就成立了，但是真正的蓬勃发展却是在二战之后的五六十年代。那段时间的美国正处于经济发展的黄金时期，人们变得越来越富有使得大家有了一定的经济实力来购买比较贵重的珠宝——钻石。这使得钻石行业在美国也进入了一个黄金时期，也使得钻石市场对于一种可以量化的衡量钻石的标准的需求愈发迫切。GIA前瞻性的发现了这一趋势，于是便在总结前人经验的基础之上提出了&quot;4C&quot;的概念。这一概念为钻石行业的发展提供了一个很重要的理论基础，虽然经过修修改改，但是核心理论却被行业沿用至今。</p>
<p>GIA 作为一个推动珠宝检定标准化的组织，同时也是最早的向大众普及这些钻石鉴定证书的推动者。GIA制作的证书强调统一，有种标准化的工业之美，由于其贴近中产阶级的风格和非常成功的宣传，使得GIA的证书越来越得到普罗大众的认可，并在90年代HRD进入中国之后在2000年后紧随其后进入中国市场，成为了国内认知度最高的国际证书之一。<br />
  时至今日GIA作为最严谨和权威的钻石证书之一，已经得到了市场和消费者充分的信任，如果你购买的是GIA证书的钻石呢，可以通过证书上的序列号在http://www.gia.edu/report-check-landing这个网站上进行查询，辨明真伪。<br />
说完了HRD和GIA，今天让我们来看一下三大钻石证书当中的最后一种，IGI证书。IGI的全称是International Gemological Institute，国际宝石学会。国际宝石学会说起来也是与HRD同宗同源，同样来自于被称为钻石之都的比利时安特卫普。虽然同属重量级的权威证书，不过跟HRD和GIA比起来，IGI可算是小弟弟了。IGI成立于1975年，正是在世界钻石交易所联盟和国际钻石制造商协会决定共同来清理市场上的劣质证书之后，其成立的目的就在于为钻石珠宝产业的从业者和消费者提供一个权威的认证平台。</p>
<img src="../images/site_elements/howtochoose/igi.jpg" alt="证书区别" />
<p>IGI可是出身名门望族，在安特卫普成立之初只为比利时少数的钻石世家提供私人钻石鉴定服务。随着一些高品质的钻石被销往欧洲的各个王室，IGI的名字便渐渐的在皇家之间传开来，并成为欧洲、中东、亚洲各个王室在面对已经镶嵌的钻石束手无策时所青睐的解决方案。IGI也渐渐的从最初的钻石鉴定发展为专门为钻石和高端首饰提供鉴定服务。<br />
  同GIA相比，IGI的客户群体有着很大的区别，这也导致了两种证书风格的不同。IGI在建立之初的主要服务对象都是名流富贾以及皇室成员等，所以IGI的证书在提供钻石分级信息的同时，每张证书也都沿用了奢侈品的手工制作程序，为的是保证各方面的品质都与钻石相匹配，就像高级的定制服装一样，为高级客户提供能体现产品特色的定制证书。IGI证书一直是受到高端产品垂青的证书，例如Dior， Montblanc等品牌在其钻石饰品系列都可以看到IGI的定制证书，一些明星例如Paris Hilton也请IGI为她的粉钻表做了钻石鉴定。<br />
  时至今日IGI也逐渐转变了他们的市场策略并开始进入大众市场，如果你买到了IGI证书的钻石，可以在 http://www.igiworldwide.com/ch/search_report.aspx 这个网站上根据证书编号来进行验证。<br />
我们讲完了HRD，GIA，IGI三大钻石证书，您也对钻石证书的来历有了些大致的了解。虽然在建立之初三者的定位和市场方向有所不同，但是由于1974年的会议中关于规范化钻石分级标准的决议，以及几十年的融合、发展、与统一，三种证书的对于钻石品质鉴定的标准已经没有什么区别了，三者都是为了保护消费者权益而制作的权威证明。就像专业人士经常说的那样&quot;与其挑选哪种证书，还不如花些心思来选择自己心仪钻石的4C来的实在&quot;。</p>
