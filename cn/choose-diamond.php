<h1>何谓荧光？</h1>
<div class="field-item even">


<img src="../images/site_elements/howtochoose/fluorescence.jpg" />


荧光反应是指钻石    在紫外荧光灯的照射下发出蓝光的性质。在天然的钻石中约有三分之一的钻石具有这种反应，钻石在紫外光下会出现荧光颜色<br />
GIA把它分成5级：无（None）、极弱（Faint）、中度（Medium）、强（Strong）、很强（Very Strong） 。<br />
荧光反应对钻石主要是有两点影响<br />
影响火光--越强的荧光会让钻石看起来越不透明<br />
改变颜色--同样是颜色H的钻石，有中度荧光的钻石看起来比较白<br />
荧光反应并不表示该颗钻石本身为蓝色，钻石成色的高低是看它本身黄色浓淡为判定标准，而不是以荧光颜色为依据。除了某些特殊的荧光外，一般来讲带荧光的钻石并不影响佩戴。<br />
根据一些最新的学术研究，其实荧光反应对钻石也有正面的影响，它会令钻石在一般正常的光线下显得更白，甚至令钻石的颜色在肉眼看下去有升级的效果，反而会使低颜色的钻石变白，同时价格更实惠。




