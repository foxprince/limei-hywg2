<h1>何谓净度</h1>



<div class="field-item even">
<img src="../images/site_elements/howtochoose/clarity.jpg" width="680" />

<p>很多朋友在购买钻石的时候一般都会很纠结钻石的净度， 经常认为花这么多钱买钻石，怎么也得买个VS以上的吧。 我想，如果您不缺钱，你就把颜色净度的指标都提到最高，这样就肯定没有什么遗憾了。但是这种买钻石的方法不是每个朋友都适用，大多数朋友的预算还是有限的，那么如何在有限的资金下买一个性价比最高的钻石呢？<br />
  首先朋友们一定要了解钻石的净度是如何评级的，这对你选购钻石是非常重要的。净度的高低是直接与钻石内部的内含物相关联的。钻石瑕疵的位置，数量，大小，颜色，方向，和可见度等都会直接影响到一颗钻石的净度评级。根据比较流行的钻石分级标准，净度可以被分为十一个不同的等级。<br />
  在所有等级中最高等级的被称为&quot;无瑕（Flawless）&quot;，这个等级的钻石完全没有任何的瑕疵，堪称极品。<br />
  接下来的等级被称为&quot;内部无瑕（Internally Flawless）&quot;，这个级别的钻石内部是完美的，只在边缘或表层有轻微的瑕疵。<br />
  在接下来的则是被称为&quot;极少内含物（Very，Very Slightly Included），VVS1/2&quot;的级别。&quot;无瑕（Flawless）&quot;， &quot;内部无瑕（InternallyFlawless）&quot;， &quot;极少内含物（Very，Very Slightly Included），VVS1/2&quot;这几个级别只有经验丰富的钻石分级师在40倍显微镜下才能看到其中的内含物。<br />
  紧随其后的则是&quot;较少内含物（Very Slightly Included），VS1/2&quot;的级别， 这个级别的钻石是在有经验的钻石分级师手中通过10倍放大镜和40倍显微镜下的辅助下才能观测到，但是比较困难。<br />
