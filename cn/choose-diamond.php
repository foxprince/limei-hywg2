<p>切工是衡量钻石价值的重要标准之一，取决于其刻面与光线的相互作用结果。在从原石到成品钻石的过程中经过了复杂的工序、精湛的技艺、还有精心的雕琢与打磨，才能完美展现钻石独有的光芒，并被专业人士称为：<br />
